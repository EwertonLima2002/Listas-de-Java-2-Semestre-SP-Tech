import java.util.Scanner;

public class MetodosCalculadora {
    int somar(int num01, int num02){
        return  num01 + num02;
    }
    int dividir(int num01, int num02){
        return  num01 / num02;
    }
    int subtrair(int num01, int num02){
        return  num01 - num02;
    }
    int multiplicacao(int num01, int num02){
        return  num01 * num02;
    }
}
